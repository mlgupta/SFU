package sfu.beans.scan;

public class ScannerInfoBean {
  String device=null;
  String model=null;

  public void setDevice(String device) {
    this.device = device;
  }

  public String getDevice() {
    return device;
  }

  public void setModel(String model) {
    this.model = model;
  }

  public String getModel() {
    return model;
  }
}
